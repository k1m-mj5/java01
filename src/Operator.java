public class Operator {
    public static void main(String[] args) {
        System.out.println(2 + 3);
        System.out.println(5 - 2);
        System.out.println(2 * 3);
        System.out.println(6 / 3); // 2
        System.out.println(7 % 3); // 1
        System.out.println(7 / 2); // 3
        System.out.println((2 - 4) / 2); // -1

        System.out.println(1 + 1.2); // 2.2
        System.out.println(7 / 2); // 3
        System.out.println(7 / 2.0); // 3.5

        System.out.println(2 < 3); // t
        System.out.println(2 <= 3); // t
        System.out.println(2 > 3);  // f
        System.out.println(3 >= 3); // t
        System.out.println(5 == 3); // f
        System.out.println(5 == 5); // t
        System.out.println(5 != 5); // f
        System.out.println(3 != 5); // t
    }
}
